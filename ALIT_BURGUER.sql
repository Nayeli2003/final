CREATE DATABASE ALIT_BURGUER;



CREATE TABLE REGISTRO(idRegistro bigint PRIMARY KEY ,
					nombre VARCHAR (30)  NOT NULL,
					apPaterno VARCHAR(30)NOT NULL,
					apMaterno VARCHAR(30)NOT NULL,
					fechaNacimiento DATE ,
					direccion VARCHAR(85)NOT NULL,
					tel_cel bigint  NOT NULL,
					cargo VARCHAR(30)NOT NULL,
					password VARCHAR(30)NOT NULL);
					

CREATE TABLE PROVEEDOR(id_proveedor bigint PRIMARY KEY,
nom_proveedor VARCHAR(20),
tipo VARCHAR(20),
fecha DATE,
precio DECIMAL,
precio_unidad DECIMAL);


CREATE TABLE PRODUCTO(id_producto bigint PRIMARY KEY ,
marca_producto VARCHAR(20),
fecha_producto DATE,
unidad_medida VARCHAR(5),
tipo VARCHAR(15),
idRegistro bigint,
FOREIGN KEY (idRegistro) REFERENCES REGISTRO(idRegistro) ON DELETE CASCADE ON UPDATE CASCADE);



CREATE TABLE surte(id_producto bigint,id_proveedor bigint,PRIMARY KEY (id_producto, id_proveedor),
FOREIGN KEY (id_producto) REFERENCES PRODUCTO(id_producto)ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (id_proveedor) REFERENCES PROVEEDOR(id_proveedor) ON DELETE CASCADE ON UPDATE CASCADE);

CREATE TABLE INVENTARIO(id_inventario bigint PRIMARY KEY,
tipo VARCHAR(35),
cantidad bigint,
area VARCHAR(30),
idRegistro bigint,
FOREIGN KEY (idRegistro) REFERENCES REGISTRO(idRegistro) ON DELETE CASCADE ON UPDATE CASCADE);



CREATE TABLE PLATILLO(id_platillo bigint PRIMARY KEY,
descripcion VARCHAR(25),
ingredientes VARCHAR(25),
precios DECIMAL,
tipo VARCHAR(10),
idRegistro bigint,
FOREIGN KEY (idRegistro) REFERENCES REGISTRO(idRegistro) ON DELETE CASCADE ON UPDATE CASCADE);

CREATE TABLE requiere(id_platillo bigint,id_producto bigint,PRIMARY KEY (id_platillo,id_producto),
FOREIGN KEY (id_platillo) REFERENCES PLATILLO(id_platillo) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (id_producto) REFERENCES PRODUCTO(id_producto)ON DELETE CASCADE ON UPDATE CASCADE);




